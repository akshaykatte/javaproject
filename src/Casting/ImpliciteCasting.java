package Casting;

public class ImpliciteCasting {

	public static void main(String[] args) {
		
		// Implicit casting --> converting lower data type into higher data type is known as primitive casting
		
		// external support not required
		
		
		int a =45;
		
		double b =a;
		System.out.println(b);
		
		
		
		

	}

}
