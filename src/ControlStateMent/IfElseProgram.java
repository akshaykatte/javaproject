package ControlStateMent;

public class IfElseProgram {

	public static void main(String[] args) {
	// if condition is true then if will get executed and condition is false then else will get executed
		
		
		// if condition is true then
		
		int mark = 45;
		
		if(mark > 35) // if will get execute
		{
			System.out.println("conditon is true ");
			
		}
		
		else
		{
			System.out.println("conditon is false");
		}
		

		
		// if condition is false 
		
		
		if(mark < 35)
		{
			System.out.println("condion is true");
			
		}
		
		else
		{
			System.out.println("condition is false");  // else will get executed
		}
		
		

	}

}
