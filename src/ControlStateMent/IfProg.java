package ControlStateMent;

public class IfProg {

	public static void main(String[] args) {

		// if the condition or statement is true then IF statement will execute
		
		int mark = 85 ;
		
		if(mark > 35)
		{
			System.out.println("condition is true");
		}
		
		

		if(mark > 50)
		{
			System.out.println("condition is true");
		}
		
		// if conditon is false then else statement will not executed 
		
		if (mark < 50)
		{
			System.out.println("will  not executed");
		}
		
		
		
	}

}
