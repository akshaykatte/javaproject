package MultiDiamentinalArray;

public class Program2 {

	public static void main(String[] args) {
	
		
		
		int arr[][]= {{10,20,30,40,50,60,},
				      
				     {11,12,13,14,15,16}};
		
		
		

		for(int i=0;i<2;i++)
		{
			for(int j=0;j<6;j++)
			{
				System.out.print(arr[i][j]+" ");
			}
			System.out.println();
		}
		
	}

}


//output   10 20 30 40 50 60 
//         11 12 13 14 15 16 

